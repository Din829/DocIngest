    def _extract_audio(self, video_path: Path) -> Path | None:
        """Extract audio track from video via ffmpeg."""
        from ..utils.binary_finder import find_binary

        ffmpeg = find_binary("ffmpeg", self.config)
        if not ffmpeg:
            logger.debug("ffmpeg not found; cannot extract audio from video")
            return None

        out_path = video_path.with_suffix(".wav")
        if out_path.exists() and out_path.stat().st_size > 0:
            return out_path  # already extracted

        try:
            subprocess.run(
                [ffmpeg, "-i", str(video_path),
                 "-vn", "-acodec", "pcm_s16le", "-ar", "16000", "-ac", "1",
                 "-y", str(out_path)],
                capture_output=True, timeout=300,
            )
            if out_path.exists() and out_path.stat().st_size > 0:
                return out_path
        except Exception as e:
            logger.debug(f"ffmpeg audio extraction failed: {e}")

        return None

    def _get_duration(self, file_path: Path) -> str:
        """Get media duration as human-readable string via ffprobe."""
        from ..utils.binary_finder import find_binary

        ffprobe = find_binary("ffprobe", self.config)
        if not ffprobe:
            return ""

        try:
            result = subprocess.run(
                [ffprobe, "-v", "quiet",
                 "-show_entries", "format=duration",
                 "-of", "default=noprint_wrappers=1:nokey=1",
                 str(file_path)],
                capture_output=True, text=True, timeout=30,
            )
            seconds = float(result.stdout.strip())
            m, s = divmod(int(seconds), 60)
            h, m = divmod(m, 60)
            if h > 0:
                return f"{h}:{m:02d}:{s:02d}"
            return f"{m}:{s:02d}"
        except Exception:
            return ""

    def _duration_seconds(self, file_path: Path) -> float:
        """Media duration in seconds via ffprobe, or 0.0 if unavailable."""
        from ..utils.binary_finder import find_binary

        ffprobe = find_binary("ffprobe", self.config)
        if not ffprobe:
            return 0.0
        try:
            result = subprocess.run(
                [ffprobe, "-v", "quiet",
                 "-show_entries", "format=duration",
                 "-of", "default=noprint_wrappers=1:nokey=1",
                 str(file_path)],
                capture_output=True, text=True, timeout=30,
            )
            return float(result.stdout.strip())
        except Exception:
            return 0.0

    @staticmethod
    def _load_info_json(file_path: Path) -> dict[str, Any] | None:
        """Load yt-dlp's .info.json if it exists alongside the media file."""
        info_path = file_path.with_suffix(".info.json")
        if not info_path.exists():
            # Also try with the video ID pattern: <id>.info.json
            for candidate in file_path.parent.glob("*.info.json"):
                info_path = candidate
                break
            else:
                return None

        try:
            with open(info_path, "r", encoding="utf-8") as f:
                return json.load(f)
        except Exception:
            return None
